package examen.services;

public class ErrorCocheException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5105051911960914310L;

	public ErrorCocheException() {
		super();
	}

	public ErrorCocheException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ErrorCocheException(String message, Throwable cause) {
		super(message, cause);
	}

	public ErrorCocheException(String message) {
		super(message);
	}

	public ErrorCocheException(Throwable cause) {
		super(cause);
	}

	
	
}
